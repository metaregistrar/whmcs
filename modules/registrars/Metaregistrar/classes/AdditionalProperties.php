    static function addFields($additionaldomainfields) {
        $relationArray  = self::getRegistryRelationArray();
        $whmcsFields    = self::getWhmcsFields();
        
        foreach($relationArray as $tld => $registry) {
            $tld = ".".$tld;
            $additionaldomainfields[$tld] = $whmcsFields[$tld];
            $registryFields = self::getRegistryFields($registry);
            
            if(is_array($additionaldomainfields[$tld])) { 
                foreach ($additionaldomainfields[$tld] as $key => $value) {
                    $additionaldomainfields[$tld][$key]["Remove"] = true;           //Remove default fields
                }
                $additionaldomainfields[$tld] = array_merge($additionaldomainfields[$tld], $registryFields);
            } else {
                $additionaldomainfields[$tld] = $registryFields;
            }
        }
        return $additionaldomainfields;
    }
    
    static function isExpectedProperty($propertyName, $registry) {
        $registryFields = self::getRegistryFields($registry);
        foreach ($registryFields as $field) {
            if($field["Name"] == $propertyName) {
                return true;
            }
        }
        return false;
    } 
